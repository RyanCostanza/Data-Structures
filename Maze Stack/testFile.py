from Stack import Stack
from lab04 import solveMaze

def test_example():
	maze = [
['+','+','+','+','G','+'],
['+',' ','+',' ',' ','+'],
['+',' ',' ',' ','+','+'],
['+',' ','+','+',' ','+'],
['+',' ',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
	assert solveMaze(maze, 4, 4) == True
	assert maze == [
['+', '+', '+', '+', 'G', '+'],
['+', 8, '+', 11, 12, '+'],
['+', 7, 9, 10, '+', '+'],
['+', 6, '+', '+', 2, '+'],
['+', 5, 4, 3, 1, '+'],
['+', '+', '+', '+', '+', '+'] ]
	
	maze2 = [
['+','+','+','+','G','+'],
['+',' ','+',' ','+','+'],
['+',' ',' ',' ','+','+'],
['+',' ','+','+',' ','+'],
['+',' ',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
	assert solveMaze(maze2, 4, 4) == False
	assert maze2 == [
['+', '+', '+', '+', 'G', '+'],
['+', 8, '+', 11, '+', '+'],
['+', 7, 9, 10, '+', '+'],
['+', 6, '+', '+', 2, '+'],
['+', 5, 4, 3, 1, '+'],
['+', '+', '+', '+', '+', '+'] ]
	
	maze3 = [
['+', '+', '+', '+', 'G', '+'],
['+', ' ', ' ', ' ', ' ', '+'],
['+', ' ', '+', '+', '+', '+'],
['+', ' ', ' ', '+', '+', '+'],
['+', '+', ' ', ' ', ' ', '+'],
['+', '+', '+', '+', '+', '+'] ]
	assert solveMaze(maze3, 4, 4) == True
	assert maze3 == [
['+', '+', '+', '+', 'G', '+'],
['+', 7, 8, 9, 10, '+'],
['+', 6, '+', '+', '+', '+'],
['+', 5, 4, '+', '+', '+'],
['+', '+', 3, 2, 1, '+'],
['+', '+', '+', '+', '+', '+'] ]


test_example()